// Evento del checkbox
const modeToggle = document.getElementById('modeToggle');
modeToggle.addEventListener('change', toggleMode);

// // Función para alternar entre los modos
function toggleMode() {
  if (modeToggle.checked) {
    applyTheme(themeColors.lightMode);
  } else {
    applyTheme(themeColors.darkMode);
  }
}

function applyTheme(theme) {
    // Recorrer las secciones de themeColors
    for (const section in theme) {
      const sectionColors = theme[section];
  
      for (const property in sectionColors) {
        const styles = sectionColors[property];
        const element = document.querySelector(`.${section} .${property}`);
  
        if (element) {
          if (typeof styles === 'object') {
            // Si hay un objeto, buscar propiedades dentro de él
            for (const styleProperty in styles) {
              if (typeof styles[styleProperty] === 'object') {
                // Si es un gradiente
                const gradient = styles[styleProperty].gradient;
                if (gradient) {
                  element.style.background = `linear-gradient(${gradient.start}, ${gradient.end})`;
                }
              } else {
                // Asignar colores
                element.style[styleProperty] = styles[styleProperty];
              }
            }
          } else {
            // Asignar directamente los colores
            element.style.color = styles;
          }
        } else {
          console.warn(`Element not found: .${section} .${property}`);
        }
      }
    }
}



// Aplicar el tema inicial
applyTheme(themeColors.darkMode);

  